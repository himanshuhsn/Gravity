package com.logigame.gravity.model;

public class Pair {
	private Integer x;
	private Integer y;
	public Pair() {
		
	}
	public Pair(Integer x, Integer y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	
}
